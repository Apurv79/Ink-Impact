import React from 'react'
import {Container, Logo, LogoutBtn} from '../index'
import { Link } from 'react-router-dom'
import {useSelector} from 'react-redux'
import { useNavigate } from 'react-router-dom'

function Header() {
  const authStatus = useSelector((state) => state.auth.status)
  const navigate = useNavigate()

  const navItems = [
    {
      name: 'Home',
      slug: "/",
      active: true
    }, 
    {
      name: "Login",
      slug: "/login",
      active: !authStatus,
  },
  {
      name: "Signup",
      slug: "/signup",
      active: !authStatus,
  },
  {
      name: "All Posts",
      slug: "/all-posts",
      active: authStatus,
  },
  {
      name: "Add Post",
      slug: "/add-post",
      active: authStatus,
  },
  ]


  return (
    <header className="py-3 shadow bg-[#defcf9] relative">
      <Container>
        <nav className="flex items-center relative mx-auto max-w-4xl rounded-full bg-[#cadefc] p-2 overflow-hidden">
          {/* Position logo so it sits partially outside the nav */}
          <div className="">
        <Link to="/">
          <Logo width="50px" />
        </Link>
      </div>
          {/* Add left padding so that nav items don't overlap the logo */}
          <ul className="flex ml-auto space-x-4 pl-20">
            {navItems.map((item) =>
              item.active ? (
                <li key={item.name}>
                  <button
                    onClick={() => navigate(item.slug)}
                    className="inline-block px-6 py-2 font-bold duration-200 hover:bg-[#defcf9] rounded-full text-black "
                  >
                    {item.name}
                  </button>
                </li>
              ) : null
            )}
            {authStatus && (
              <li>
                <LogoutBtn />
              </li>
            )}
          </ul>
        </nav>
      </Container>
    </header>
  )
}

export default Header